#include <iostream>

using namespace std;

int main()
{
	int index = 2;

	for (;;) // <= Do Not Edit This Line
	{
		// If index variable is bigger than 8, then stop the loop (break)
		if (index > 8)
			break;

		cout << index << "\n"; // From 2 To 10

		index++;
	}

	// Output
	// 2
	// 3
	// 4
	// 5
	// 6
	// 7
	// 8

	return 0;
}
